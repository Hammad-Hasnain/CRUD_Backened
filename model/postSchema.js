import mongoose, { model } from "mongoose";



const schema = new mongoose.Schema({

    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },

})


const postModel = mongoose.model('TodoUser', schema)

export default postModel